/**
 * Check if an email address uses a disposable/throwaway domain.
 *
 * @param email - The email address to check
 * @returns true if the domain is disposable, false if it's legitimate
 *
 * @example
 * isDisposableEmail('user@mailinator.com') // true
 * isDisposableEmail('user@gmail.com')      // false
 */
export function isDisposableEmail(email: string): boolean {
  const domain = email.split('@')[1]?.toLowerCase();
  if (!domain) return false;
  return DISPOSABLE_DOMAINS.has(domain);
}

