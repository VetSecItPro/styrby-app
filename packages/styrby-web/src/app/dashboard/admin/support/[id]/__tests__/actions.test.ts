/**
 * Tests for requestSupportAccessAction — actions.ts
 *
 * Phase 4.2 — Support Tooling T4
 * Updated 2026-04-25 — SEC-ADV-001 remediation:
 *   - Cookie-channel coverage replaced with admin_stash_grant_token coverage.
 *   - The action now performs two RPCs: admin_request_support_access (writes
 *     hash + grant row) followed by admin_stash_grant_token (writes raw token
 *     to the server-only support_grant_token_pickup table).
 *   - The non-HttpOnly cookie has been deleted from the action; tests verify
 *     no cookies API is touched at all.
 *
 * Coverage:
 *   (a) Zod validation — invalid inputs return { ok: false, field } without
 *       calling any RPC.
 *   (b) SQLSTATE mapping for admin_request_support_access — 42501 → "Not
 *       authorized", 22023 → "Invalid session…", 23514 → "Reason is required",
 *       unknown → "Internal error" + Sentry.
 *   (c) Happy path — calls request RPC then stash RPC with correct args; never
 *       writes a cookie; redirects to success URL.
 *   (d) Cross-user session rejection — 22023 from request RPC.
 *   (e) trustedTicketId integrity — bound URL ID is what reaches the request RPC.
 *   (f) Raw token never appears in the action return value or Sentry extras.
 *   (g) expires_in_hours bounds — 0 and 169 rejected; 1 and 168 accepted.
 *   (h) reason bounds — < 10 chars rejected; 10 chars accepted; > 500 chars rejected.
 *   (i) Stash RPC failure surfaces a user-facing error and skips the redirect.
 *   (j) Stash RPC receives the same grantId returned by request RPC and the
 *       same raw token generated by generateSupportToken.
 *
 * SOC 2 CC6.1 / CC7.2: admin action contract (auth check, audit, token safety)
 * is fully covered so compliance reviews can reference test output.
 */

