// ─── Helpers ──────────────────────────────────────────────────────────────────

/**
 * Builds a FormData from a plain record.
 */
function makeFormData(entries: Record<string, string | null | undefined>): FormData {
  const fd = new FormData();
  for (const [key, value] of Object.entries(entries)) {
    if (value != null) fd.append(key, value);
  }
  return fd;
}

const VALID_TICKET_ID = 'a1b2c3d4-e5f6-7890-abcd-ef1234567890';
const VALID_SESSION_ID = 'b2c3d4e5-f6a7-8901-bcde-f12345678901';
const VALID_GRANT_ID = '42';
const VALID_USER_ID = 'ccccdddd-eeee-ffff-aaaa-bbbbccccdddd';
/** Acting admin ID returned by auth.getUser() for assertAdminMfa wiring tests. */
const ACTING_ADMIN_ID = 'aaaabbbb-cccc-dddd-eeee-ffffaaaabbbb';

/** Minimal valid form data for happy-path tests. */
function validFormData(overrides: Record<string, string> = {}): FormData {
  return makeFormData({
    session_id: VALID_SESSION_ID,
    reason: 'User reported cost spike — investigating session tool call pattern.',
    expires_in_hours: '24',
    ...overrides,
  });
}

/**
 * Configure mockRpc to dispatch by RPC name. The default behaviour:
 *   - admin_request_support_access → returns VALID_GRANT_ID
 *   - admin_stash_grant_token      → returns null/no error
 *
 * Tests can override per-call with `.mockImplementationOnce` if needed, or
 * supply per-name overrides via this helper.
 */
function configureRpc(overrides?: {
  request?: { data: unknown; error: unknown };
  stash?: { data: unknown; error: unknown };
}) {
  const requestResult = overrides?.request ?? { data: VALID_GRANT_ID, error: null };
  const stashResult = overrides?.stash ?? { data: null, error: null };
  mockRpc.mockImplementation(async (fnName: string) => {
    if (fnName === 'admin_request_support_access') return requestResult;
    if (fnName === 'admin_stash_grant_token') return stashResult;
    return { data: null, error: null };
  });
}

// ─── Test suites ──────────────────────────────────────────────────────────────

describe('requestSupportAccessAction', () => {
  beforeEach(() => {
    vi.clearAllMocks();

    // WHY rebuild from() chain each test: state would otherwise leak when a
    // test modifies the chain. Default returns the ticket's user_id for any
    // ticket UUID — sufficient for happy-path and error-path tests alike.
    const mockMaybeSingle = vi.fn().mockResolvedValue({
      data: { user_id: VALID_USER_ID },
      error: null,
    });
    const mockEq = vi.fn().mockReturnValue({ maybeSingle: mockMaybeSingle });
    const mockSelect = vi.fn().mockReturnValue({ eq: mockEq });
    mockFrom.mockReturnValue({ select: mockSelect });

    // WHY include auth.getUser: H42 Layer 1 added auth.getUser() calls inside
    // admin actions to resolve the acting admin for the MFA gate (assertAdminMfa).
    // The mock must return a valid admin user so the gate is genuinely exercised.
    (createClient as Mock).mockResolvedValue({
      rpc: mockRpc,
      from: mockFrom,
      auth: {
        getUser: vi.fn().mockResolvedValue({
          data: { user: { id: ACTING_ADMIN_ID, email: 'admin@styrby.test' } },
        }),
      },
    });

    // Default: both RPCs succeed.
    configureRpc();
  });

  // ── (a) Zod validation ─────────────────────────────────────────────────────

  it('(a) rejects invalid session_id UUID', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(
      VALID_TICKET_ID,
      makeFormData({
        session_id: 'not-a-uuid',
        reason: 'Valid reason with enough characters.',
        expires_in_hours: '24',
      })
    );

    expect(result).toMatchObject({ ok: false, field: 'session_id' });
    expect(mockRpc).not.toHaveBeenCalled();
  });

  it('(a) rejects reason shorter than 10 chars', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(
      VALID_TICKET_ID,
      makeFormData({
        session_id: VALID_SESSION_ID,
        reason: 'Short',
        expires_in_hours: '24',
      })
    );

    expect(result).toMatchObject({ ok: false, field: 'reason' });
    expect(mockRpc).not.toHaveBeenCalled();
  });

  it('(h) accepts reason of exactly 10 chars', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(
        VALID_TICKET_ID,
        makeFormData({
          session_id: VALID_SESSION_ID,
          reason: '1234567890',
          expires_in_hours: '24',
        })
      )
    ).rejects.toThrow(/NEXT_REDIRECT/);

    // Both RPCs must have been called.
    const calls = mockRpc.mock.calls.map((c) => c[0]);
    expect(calls).toContain('admin_request_support_access');
    expect(calls).toContain('admin_stash_grant_token');
  });

  it('(h) rejects reason longer than 500 chars', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(
      VALID_TICKET_ID,
      makeFormData({
        session_id: VALID_SESSION_ID,
        reason: 'A'.repeat(501),
        expires_in_hours: '24',
      })
    );

    expect(result).toMatchObject({ ok: false, field: 'reason' });
    expect(mockRpc).not.toHaveBeenCalled();
  });

  it('(g) rejects expires_in_hours = 0', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(
      VALID_TICKET_ID,
      makeFormData({
        session_id: VALID_SESSION_ID,
        reason: 'Valid reason with enough characters.',
        expires_in_hours: '0',
      })
    );

    expect(result).toMatchObject({ ok: false, field: 'expires_in_hours' });
    expect(mockRpc).not.toHaveBeenCalled();
  });

  it('(g) rejects expires_in_hours = 169', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(
      VALID_TICKET_ID,
      makeFormData({
        session_id: VALID_SESSION_ID,
        reason: 'Valid reason with enough characters.',
        expires_in_hours: '169',
      })
    );

    expect(result).toMatchObject({ ok: false, field: 'expires_in_hours' });
    expect(mockRpc).not.toHaveBeenCalled();
  });

  it('(g) accepts expires_in_hours = 1 (boundary)', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData({ expires_in_hours: '1' }))
    ).rejects.toThrow(/NEXT_REDIRECT/);
  });

  it('(g) accepts expires_in_hours = 168 (boundary)', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData({ expires_in_hours: '168' }))
    ).rejects.toThrow(/NEXT_REDIRECT/);
  });

  // ── (b) SQLSTATE mapping ───────────────────────────────────────────────────

  it('(b) maps SQLSTATE 42501 to "Not authorized"', async () => {
    configureRpc({ request: { data: null, error: { code: '42501' } } });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toEqual({ ok: false, error: 'Not authorized' });
    // Stash must NOT be called once request fails.
    const stashCalls = mockRpc.mock.calls.filter((c) => c[0] === 'admin_stash_grant_token');
    expect(stashCalls.length).toBe(0);
  });

  it('(b) maps SQLSTATE 22023 to invalid-session message', async () => {
    configureRpc({
      request: {
        data: null,
        error: { code: '22023', message: 'session does not belong to ticket user' },
      },
    });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toMatchObject({ ok: false });
    expect((result as { ok: false; error: string }).error).toContain('Invalid session');
  });

  it('(b) maps SQLSTATE 23514 to "Reason is required"', async () => {
    configureRpc({ request: { data: null, error: { code: '23514' } } });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toEqual({ ok: false, error: 'Reason is required' });
  });

  it('(b) maps unknown SQLSTATE to "Internal error" and captures Sentry', async () => {
    configureRpc({
      request: { data: null, error: { code: 'P0001', message: 'unexpected rpc error' } },
    });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toMatchObject({ ok: false, error: 'Internal error — check Sentry' });
    expect(mockSentryCaptureException).toHaveBeenCalled();
    const [, sentryCtx] = mockSentryCaptureException.mock.calls[0];
    expect(sentryCtx.tags.admin_action).toBe('request_support_access');
  });

  // ── (c) Happy path ─────────────────────────────────────────────────────────

  it('(c) calls request RPC with correct args (p_user_id from ticket; no p_ip / p_ua)', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData())
    ).rejects.toThrow(/NEXT_REDIRECT/);

    const requestCall = mockRpc.mock.calls.find(
      (c) => c[0] === 'admin_request_support_access'
    );
    expect(requestCall).toBeDefined();
    expect(requestCall![1]).toEqual({
      p_ticket_id: VALID_TICKET_ID,
      p_user_id: VALID_USER_ID,
      p_session_id: VALID_SESSION_ID,
      p_reason: 'User reported cost spike — investigating session tool call pattern.',
      p_expires_in_hours: 24,
      p_token_hash: MOCK_HASH,
    });
    const requestArgs = requestCall![1] as Record<string, unknown>;
    expect(requestArgs).not.toHaveProperty('p_ip');
    expect(requestArgs).not.toHaveProperty('p_ua');
  });

  it('(c) does NOT touch cookies — replaces the prior non-HttpOnly cookie channel (SEC-ADV-001)', async () => {
    // The action's import list no longer pulls `cookies` from next/headers.
    // We assert this indirectly: if cookies were called, our next/headers
    // mock (which exposes only `headers`) would throw on the missing export.
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData())
    ).rejects.toThrow(/NEXT_REDIRECT/);

    // No assertion needed beyond the redirect — the test passes only if the
    // action completed without invoking the (non-existent) cookies() helper.
  });

  it('(c) redirects to success page with grant ID', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData())
    ).rejects.toThrow(/NEXT_REDIRECT/);

    expect(mockRedirect).toHaveBeenCalledWith(
      `/dashboard/admin/support/${VALID_TICKET_ID}/request-access/success?grant=${VALID_GRANT_ID}`
    );
    expect(mockRevalidatePath).toHaveBeenCalledWith(
      `/dashboard/admin/support/${VALID_TICKET_ID}`
    );
  });

  // ── (d) Cross-user session rejection via request RPC ───────────────────────

  it('(d) returns invalid-session error when request RPC raises 22023 (cross-user session)', async () => {
    configureRpc({
      request: {
        data: null,
        error: { code: '22023', message: 'session user_id does not match ticket user_id' },
      },
    });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toMatchObject({ ok: false });
    expect((result as { ok: false; error: string }).error).toContain('Invalid session');
    expect(mockRedirect).not.toHaveBeenCalled();
  });

  // ── (e) Ticket ID binding integrity ───────────────────────────────────────

  it('(e) passes the trustedTicketId from .bind() to the request RPC', async () => {
    const ANOTHER_TICKET = 'ffffffff-1111-2222-3333-444444444444';
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(ANOTHER_TICKET, validFormData())
    ).rejects.toThrow(/NEXT_REDIRECT/);

    const requestCall = mockRpc.mock.calls.find(
      (c) => c[0] === 'admin_request_support_access'
    );
    expect(requestCall![1]).toMatchObject({
      p_ticket_id: ANOTHER_TICKET,
      p_user_id: VALID_USER_ID,
    });
  });

  // ── (f) Raw token leakage prevention ──────────────────────────────────────

  it('(f) does not include raw token in Sentry extras on request-RPC error', async () => {
    configureRpc({
      request: { data: null, error: { code: 'P0001', message: 'unexpected' } },
    });
    const { requestSupportAccessAction } = await import('../actions');

    await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    const sentryExtra = mockSentryCaptureException.mock.calls[0]?.[1]?.extra ?? {};
    expect(JSON.stringify(sentryExtra)).not.toContain(MOCK_RAW_TOKEN);
  });

  it('(f) does not include raw token in the action return value on error', async () => {
    configureRpc({ request: { data: null, error: { code: '42501' } } });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(JSON.stringify(result)).not.toContain(MOCK_RAW_TOKEN);
  });

  // ── (i) Stash RPC failure path ─────────────────────────────────────────────

  it('(i) surfaces a user-facing error and skips redirect when stash RPC fails', async () => {
    configureRpc({
      stash: {
        data: null,
        error: { code: 'P0001', message: 'unique violation or auth error' },
      },
    });
    const { requestSupportAccessAction } = await import('../actions');

    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toMatchObject({ ok: false });
    expect((result as { ok: false; error: string }).error).toContain('Token could not be stored');
    // Redirect must not have been called.
    expect(mockRedirect).not.toHaveBeenCalled();
    // Sentry must capture the stash failure.
    expect(mockSentryCaptureException).toHaveBeenCalled();
    const [, sentryCtx] = mockSentryCaptureException.mock.calls[0];
    expect(sentryCtx.tags.admin_action).toBe('stash_grant_token');
    // Raw token must never reach Sentry — even on stash failure.
    expect(JSON.stringify(sentryCtx.extra ?? {})).not.toContain(MOCK_RAW_TOKEN);
  });

  // ── (j) Stash RPC arg fidelity ────────────────────────────────────────────

  it('(j) calls stash RPC with the grantId from request RPC and the raw token from generateSupportToken', async () => {
    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData())
    ).rejects.toThrow(/NEXT_REDIRECT/);

    const stashCall = mockRpc.mock.calls.find((c) => c[0] === 'admin_stash_grant_token');
    expect(stashCall).toBeDefined();
    expect(stashCall![1]).toEqual({
      p_grant_id: VALID_GRANT_ID,
      p_raw_token: MOCK_RAW_TOKEN,
    });
  });

  it('(j) the request RPC is called BEFORE the stash RPC (order matters)', async () => {
    const callOrder: string[] = [];
    mockRpc.mockImplementation(async (fnName: string) => {
      callOrder.push(fnName);
      if (fnName === 'admin_request_support_access') {
        return { data: VALID_GRANT_ID, error: null };
      }
      if (fnName === 'admin_stash_grant_token') {
        return { data: null, error: null };
      }
      return { data: null, error: null };
    });

    const { requestSupportAccessAction } = await import('../actions');

    await expect(
      requestSupportAccessAction(VALID_TICKET_ID, validFormData())
    ).rejects.toThrow(/NEXT_REDIRECT/);

    expect(callOrder).toEqual([
      'admin_request_support_access',
      'admin_stash_grant_token',
    ]);
  });

  // ── (MFA gate) H42 Layer 1 wiring proof ──────────────────────────────────────

  // WHY: proves requestSupportAccessAction calls assertAdminMfa before running any
  // RPC. If the gate throws AdminMfaRequiredError, the action must short-circuit
  // and return { ok: false, error: 'ADMIN_MFA_REQUIRED' } without calling any RPC.
  // OWASP A07:2021, SOC 2 CC6.1.
  it('(MFA gate) returns ADMIN_MFA_REQUIRED and skips both RPCs when assertAdminMfa throws', async () => {
    (assertAdminMfa as Mock).mockRejectedValueOnce(new AdminMfaRequiredError());

    const { requestSupportAccessAction } = await import('../actions');
    const result = await requestSupportAccessAction(VALID_TICKET_ID, validFormData());

    expect(result).toEqual({ ok: false, error: 'ADMIN_MFA_REQUIRED' });
    expect(assertAdminMfa).toHaveBeenCalledWith(ACTING_ADMIN_ID);
    expect(mockRpc).not.toHaveBeenCalled();
  });
});
