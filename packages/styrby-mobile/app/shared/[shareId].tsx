/**
 * Shared Session Viewer Screen
 *
 * Public viewer for a shared Styrby session replay. No authentication is
 * required — the share link alone grants access. Messages are E2E encrypted;
 * the viewer must enter the decryption key (obtained from the session owner
 * via a secure channel) to read message content.
 *
 * Deep link: styrby://shared/{shareId}
 *
 * @route /shared/:shareId
 * @auth None required (public)
 */

