/**
 * Public session metadata returned by the shared-session API.
 * Excludes sensitive fields like user_id and machine_id.
 */
interface SharedSessionData {
  /** Session UUID */
  id: string;
  /** Display title (may be null for untitled sessions) */
  title: string | null;
  /** AI-generated summary (may be null) */
  summary: string | null;
  /** Agent type string (e.g. 'claude', 'codex') */
  agentType: string;
  /** Session completion status */
  status: string;
  /** Total session cost in USD */
  totalCostUsd: number;
  /** Total message count */
  messageCount: number;
  /** ISO 8601 session start timestamp */
  startedAt: string;
  /** ISO 8601 session end timestamp (null if still active) */
  endedAt: string | null;
}

/**
 * Encrypted message included in the shared session response.
 * Content remains encrypted until the viewer provides the decryption key.
 */
interface SharedMessageData {
  /** Message UUID */
  id: string;
  /** Ordering position (ascending = chronological) */
  sequenceNumber: number;
  /** Message type drives rendering: 'user_prompt' | 'agent_response' | ... */
  messageType: string;
  /** Base64-encoded NaCl-box ciphertext (null if message was never encrypted) */
  contentEncrypted: string | null;
  /** Base64-encoded nonce used during encryption (null if not encrypted) */
  encryptionNonce: string | null;
  /** Duration in milliseconds for agent responses (null for user messages) */
  durationMs: number | null;
  /** Input tokens consumed by this message */
  inputTokens: number | null;
  /** Output tokens generated by this message */
  outputTokens: number | null;
  /** Cache tokens saved by this message */
  cacheTokens: number | null;
  /** ISO 8601 creation timestamp */
  createdAt: string;
}

/**
 * Share record metadata returned alongside session and messages.
 */
interface ShareRecord {
  shareId: string;
  sessionId: string;
  expiresAt: string | null;
  accessCount: number;
  maxAccesses: number | null;
  createdAt: string;
}

/**
 * Full API response shape from GET /api/shared/[shareId].
 */
interface ShareApiResponse {
  share: ShareRecord;
  session: SharedSessionData;
  messages: SharedMessageData[];
}

/**
 * A message after a decryption attempt.
 */
interface DecryptedMessage extends SharedMessageData {
  /** Decrypted plaintext, or null if not yet decrypted / decryption failed */
  content: string | null;
  /** True if this message was E2E encrypted (had a nonce) */
  wasEncrypted: boolean;
}

/**
 * Current load state for the viewer.
 */
type LoadState = 'loading' | 'ready' | 'expired' | 'not_found' | 'error';

