/**
 * Formats a cost value in USD to a compact display string.
 *
 * @param costUsd - Cost amount in USD
 * @returns Formatted string e.g. "$0.0042" or "< $0.0001"
 */
function formatCost(costUsd: number): string {
  if (costUsd === 0) return '$0.00';
  if (costUsd < 0.0001) return '< $0.0001';
  return `$${costUsd.toFixed(4)}`;
}

/**
 * Formats a session duration in minutes to a human-readable string.
 *
 * @param minutes - Duration in minutes
 * @returns Formatted string e.g. "1h 23m" or "45m"
 */
function formatDuration(minutes: number): string {
  if (minutes < 60) return `${Math.round(minutes)}m`;
  const h = Math.floor(minutes / 60);
  const m = Math.round(minutes % 60);
  return m > 0 ? `${h}h ${m}m` : `${h}h`;
}

/**
 * Estimates the per-message cost from token counts using Sonnet-4 pricing.
 *
 * @param input - Input tokens
 * @param output - Output tokens
 * @param cache - Cache tokens
 * @returns Formatted cost string or null if tokens are zero
 */
function messageCostLabel(
  input: number | null,
  output: number | null,
  cache: number | null
): string | null {
  const total = (input ?? 0) + (output ?? 0) + (cache ?? 0);
  if (total === 0) return null;
  const estimated = ((input ?? 0) * 3 + (output ?? 0) * 15 + (cache ?? 0) * 0.30) / 1_000_000;
  if (estimated === 0) return null;
  if (estimated < 0.0001) return '< $0.0001';
  return `$${estimated.toFixed(4)}`;
}

/**
 * Attempts a very simple base64 decode to Uint8Array.
 *
 * WHY: We can't import the styrby-shared `decodeBase64` helper in a public
 * screen that has no auth context (and to keep the bundle minimal). This
 * fallback uses the global `atob` which is available in Hermes/JSC.
 *
 * @param b64 - Base64 encoded string
 * @returns Decoded bytes
 * @throws If the string is not valid base64
 */
function decodeBase64Simple(b64: string): Uint8Array {
  const binary = atob(b64.trim());
  const bytes = new Uint8Array(binary.length);
  for (let i = 0; i < binary.length; i++) {
    bytes[i] = binary.charCodeAt(i);
  }
  return bytes;
}

